static uint16_t buff[CHAR_MAX_BUFF_SIZE];

void LCD_FillRect(uint16_t x, uint16_t y, uint16_t width, uint16_t height)
{
	
	while(hspi1.State != HAL_SPI_STATE_READY)
		{
			//osDelay(1);
		};	
		
	for(uint16_t i=0;i<528;i++)
	{
		buff[i]=LCD_backgroundColor;
	}
	
	LCD_SetCursorPosition(x, y, x+width-1, y+height-1);
	LCD_SendCommand(ILI9341_GRAM);
	TFT_DC_SET;
	TFT_CS_RESET;

	uint32_t forFill=width*height;
	
	while(forFill!=0)
	{
		uint16_t fillNow;
		if(forFill>528)
		{
			fillNow=528;
		}
		else
		{
			fillNow=forFill;
		}
		
		while(hspi1.State != HAL_SPI_STATE_READY)
		{
			//osDelay(1);
		};		
		
		HAL_SPI_Transmit_DMA(&hspi1, (uint8_t*)&buff[0], fillNow*2);
		//HAL_SPI_Transmit(&hspi1, (uint8_t*)&buff[0], fillNow*2,100);
		
		forFill-=fillNow;
	}
}

void LCD_WaitAndReleaseSPI()
{
	while( hspi1.State != HAL_SPI_STATE_READY)
	{
		//osDelay(1);
	};

	TFT_CS_SET;	
}

int LCD_Putchar(uint16_t x0, uint16_t y0, unsigned char s)
{
	if(s>191)s-=65;
	
		while(hspi1.State != HAL_SPI_STATE_READY)
		{
			//osDelay(1);
		};
		
		struct FONT_CHAR_INFO fi=microsoftSansSerif_19ptDescriptors[s-32];
		uint8_t curSymbolW=fi.width;
		uint16_t i=fi.start_pos;
		
		int pos=0;
		for(uint8_t l=0;l<=CHAR_H_PXL;l++)
		{
			for(uint8_t bp=0;bp<5;bp++)
			{
				uint8_t curxpos;
				uint8_t dat=microsoftSansSerif_19ptBitmaps[i++];
				for(uint8_t bb=1;bb<=8;bb++)
				{
					curxpos=bp*8+bb;
					
					if((dat << bb ) & 0x100)
					{ 
						buff[pos++]=LCD_foregraundColor;
					}
					else
					{
						buff[pos++]=LCD_backgroundColor;
					}
					
					if(curxpos ==curSymbolW)break;
				}	
				if(curxpos==curSymbolW)break;
				
			}	
		}
	
		LCD_SetCursorPosition(x0, y0, x0+curSymbolW-1, y0+CHAR_H_PXL-1);
		LCD_SendCommand(ILI9341_GRAM);
		TFT_DC_SET;
		TFT_CS_RESET;
		HAL_SPI_Transmit_DMA(&hspi1, (uint8_t*)&buff[0], curSymbolW*2*CHAR_H_PXL);

		LCD_FillRect(x0+curSymbolW, y0, 1, CHAR_H_PXL);
	
		return x0+curSymbolW+1;
}

uint16_t LCD_DrawString(uint16_t x, uint16_t y, char *str, bool fillLine)
{
    while(*str)
    {
       x = LCD_Putchar(x,y,*str++);
    }
		if(fillLine)
		{
			LCD_FillRect(x, y, 320-x-1, CHAR_H_PXL);
		}
		
		return x;
}

