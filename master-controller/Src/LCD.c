uint16_t LCD_RGB(float r, float g, float b)
{
	uint16_t rw=(uint16_t)(r*31);
	uint16_t gw=(uint16_t)(g*63);
	uint16_t bw=(uint16_t)(b*31);
	
	uint16_t res=rw<<11 | gw<<5 | bw;
	
	uint16_t res2= ((res&0xff)<<8) | (res>>8);
	
	return res2;
}

